#OPERADORES ARITIMETICOS
"""
	() - Parenteses
	** - Exponenciacao
	*  - Multiplicacao
	/  - Divisao
	// - Divisao Inteira
	%  - Resto da divisao
	+  - Adicao
	-  - Subtracao
"""

a = 2
b = 5
total = a ** b
print(f"A= %f ** B= %f \n-----------------------------\nTOTAL= %f\n\n"  % (a,b,total))

total = a * b
print(f"A= %f * B= %f  \n-----------------------------\nTOTAL= %f\n\n"  % (a,b,total))

total = a / b
print(f"A= %f / B= %f  \n-----------------------------\nTOTAL= %f\n\n"  % (a,b,total))

total = a // b
print(f"A= %f // B= %f \n-----------------------------\nTOTAL= %f\n\n"  % (a,b,total))

total = a % b
print(f"A= %f  B= %f   \n-----------------------------\nTOTAL= %f\n\n"  % (a,b,total))

total = a + b
print(f"A= %f + B= %f  \n-----------------------------\nTOTAL= %f\n\n"  % (a,b,total))

total = a - b
print(f"A= %f - B= %f  \n-----------------------------\nTOTAL= %f\n\n"  % (a,b,total))

